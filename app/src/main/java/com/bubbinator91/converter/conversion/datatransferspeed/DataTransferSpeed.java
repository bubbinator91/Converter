package com.bubbinator91.converter.conversion.datatransferspeed;

/**
 * Coordinates getting an instance of the specific data transfer speed conversion class to convert
 * from.
 */
public class DataTransferSpeed {
    public DataTransferSpeed() {}

    public BitsPerSecond BitsPerSecond() {
        return BitsPerSecond.getInstance();
    }

    public BytesPerSecond BytesPerSecond() {
        return BytesPerSecond.getInstance();
    }

    public GigabitsPerSecond GigabitsPerSecond() {
        return GigabitsPerSecond.getInstance();
    }

    public GigabytesPerSecond GigabytesPerSecond() {
        return GigabytesPerSecond.getInstance();
    }

    public KilobitsPerSecond KilobitsPerSecond() {
        return KilobitsPerSecond.getInstance();
    }

    public KilobytesPerSecond KilobytesPerSecond() {
        return KilobytesPerSecond.getInstance();
    }

    public MegabitsPerSecond MegabitsPerSecond() {
        return MegabitsPerSecond.getInstance();
    }

    public MegabytesPerSecond MegabytesPerSecond() {
        return MegabytesPerSecond.getInstance();
    }

    public TerabitsPerSecond TerabitsPerSecond() {
        return TerabitsPerSecond.getInstance();
    }

    public TerabytesPerSecond TerabytesPerSecond() {
        return TerabytesPerSecond.getInstance();
    }
}
